class Solution 
{
    public:
    static bool myComp(Job A,Job B){
        
        return A.profit>B.profit;
    }
    //Function to find the maximum profit and the number of jobs done.
    vector<int> JobScheduling(Job arr[], int n) 
    { 
        sort(arr,arr+n,myComp);
        int deadline=0;
        int count=0;
        int maxProfit=0;
        vector<bool> timeSlot(n+1,false);
        for(int i=0;i<n;i++){
            Job el=arr[i];
            // cout<<el.dead<<" "<<el.profit<<endl;
            int index=el.dead;
            while(index>0 && timeSlot[index]){
                index--;
            }
            if(index>0){
                count++;
                timeSlot[index]=true;
                maxProfit+=el.profit;
            }
        }
        vector<int> ans={count,maxProfit};
        
        return ans;
    } 
};

